n1 = int(input('Digite o 1° valor: '))
n2 = int(input('Digite o 2° valor: '))
soma = n1 + n2
multiplicacao = n1 * n2
divisao = n1 / n2
divInteira = n1 // n2
expoente = n1 ** n2
print('--------------------------')
print(f'A soma entre {n1} e {n2} vale {soma}')    
print(f'A multiplicação entre {n1} e {n2} vale {multiplicacao}')
print(f'A divisão entre {n1} e {n2} vale {divisao}')
print(f'A divisão inteira entre {n1} e {n2} vale {divInteira}')
print(f'O expoente entre {n1} e {n2} vale {expoente}')
print('--------------------------')